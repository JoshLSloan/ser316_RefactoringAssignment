package main.java.memoranda;

public class Time {

    private int hour;
    private int minute;
    
    public Time(int parmHour, int parmMinute) {
        this.hour = parmHour;
        this.minute = parmMinute;
    }

    public int getHour() {
        return hour;
    }

    public int getMinute() {
        return minute;
    }
    
    
}
